# gets.chomp will take an input from the user and set it to the variable
# simple gets.chomp is a simple string response from the user
print "How old are you? "
age = gets.chomp
print "How tall are you? "
height = gets.chomp
print "How much do you weight? "
weight = gets.chomp

puts "So, you're #{age} years old, #{height} inches tall and #{weight}lbs heavy."

